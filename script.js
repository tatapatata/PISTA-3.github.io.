    function handlePixelClick(pixel) {
        const requiredColor = pixel.dataset.value;
        if (selectedColor && selectedColor == requiredColor) {
            pixel.style.backgroundColor = colors[selectedColor];
            pixel.textContent = '';
            pixel.classList.add('colored');
            paintedPixelsCount++;
            checkCompletion();
        }
    }

